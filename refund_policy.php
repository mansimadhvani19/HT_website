        <h1>Refund Policy</h1>
        
        <p>At Happy Team Company, we value your trust and strive to provide outstanding event planning services. We understand that circumstances may require event cancellations, and we have established a comprehensive refund policy to address different event types and cancellation timelines.</p>
        
        <h2>1. Small Events (e.g., Birthday, Reception, Business or Corporate Event)</h2>
        <p>For small events, including birthdays, receptions, and business or corporate events, please note that there will be no refunds upon cancellations. We kindly request our clients to carefully consider their event plans before finalizing their bookings.</p>
        
        <h2>2. Big Events (e.g., Wedding)</h2>
        <p>For significant events like weddings, our refund policy is as follows:</p>
        
        <ol type="a">
            <li>
                Cancellation before 60 days:<br>
                In the event of cancellation at least 60 days prior to the scheduled date, a full refund will be provided, excluding any non-refundable expenses already incurred by Happy Team Company.
            </li>
            <li>
                Cancellation between 60 and 45 days:<br>
                If the event is cancelled between 60 and 45 days before the scheduled date, a refund of 75% of the total event management fee will be provided, excluding any non-refundable expenses already incurred by Happy Team Company.
            </li>
            <li>
                Cancellation between 45 and 30 days:<br>
                If the event is cancelled between 45 and 30 days before the scheduled date, a refund of 50% of the total event management fee will be provided, excluding any non-refundable expenses already incurred by Happy Team Company.
            </li>
            <li>
                Cancellation between 30 and 20 days:<br>
                If the event is cancelled between 30 and 20 days before the scheduled date, a refund of 30% of the total event management fee will be provided, excluding any non-refundable expenses already incurred by Happy Team Company.
            </li>
            <li>
                Cancellation within 20 days:<br>
                Regrettably, no refunds will be provided for cancellations made within 20 days of the scheduled event date.
            </li>
        </ol>
        
        <p><strong>Note:</strong> For events such as sports tournaments (marathon and cricket), there will be no refunds applicable from the Happy Team Event Management company.</p>
        
        <h2>3. Non-Refundable Expenses</h2>
        <p>Please be aware that certain expenses, such as venue deposits, permits, and third-party vendor fees, are often non-refundable. These non-refundable expenses will be deducted from any eligible refund amount.</p>
        
        <h2>4. Refund Process</h2>
        <p>To initiate a refund request, please contact our dedicated customer support team at <a href="mailto:ask@happyteam.co.in">ask@happyteam.co.in</a>. Kindly provide your full name, contact information, event details, and a clear explanation for the refund request. Our team will carefully review your request and respond within 10 business days with a confirmation and further instructions regarding the refund process.</p>
        
        <p><strong>Important:</strong> This refund policy exclusively applies to our event management services. Any refunds or cancellations related to third-party services, such as catering, entertainment, or transportation, will be subject to the policies and terms outlined by those specific vendors.</p>
        
        <p>We appreciate your understanding of our refund policy, which allows us to maintain the highest standards of service and professionalism. If you have any inquiries or concerns regarding our policy, please do not hesitate to reach out to our customer support team.</p>
        
